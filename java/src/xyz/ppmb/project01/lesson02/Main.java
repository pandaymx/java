public class Main {
    public static void main(String[] args) {
        // 输出10
        System.out.println(10);
        // 输出二进制数1011101
        System.out.println(0b1011101);
        // 输出八进制数1756431
        System.out.println(01756431);
        // 输出十六进制数0x4971bd
        System.out.println(0x4971bd);
        // 输出浮点数3.14001
        System.out.println(3.14001);
        // 输出科学计数法表示的浮点数2.5e6
        System.out.println(2.5e6);
        // 输出布尔值true
        System.out.println(true);
        // 输出布尔值false
        System.out.println(false);
        // 输出字符'中'
        System.out.println('中');
        // 输出字符串"Hello World"
        System.out.println("Hello World");
        // 不能直接输出null
        // System.out.println(null);
        String a = null;
        // 输出null
        System.out.println(a);
    }
}
