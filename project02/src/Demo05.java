public class Demo05 {
    public static void main(String[] args) {
        int a = 10;
        int b = 4;
        // 算数运算符
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        // 有浮点数参与运算，才能得到浮点数
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
